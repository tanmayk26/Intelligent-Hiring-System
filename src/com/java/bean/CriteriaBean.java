package com.java.bean;

public class CriteriaBean {

	private int criteria_id;
	private String total_year_of_exp;
	private String technology;
	private String qualification;
	
	
	public String getQualification() {
		return qualification;
	}
	public void setQualification(String qualification) {
		this.qualification = qualification;
	}
	public int getCriteria_id() {
		return criteria_id;
	}
	public void setCriteria_id(int criteria_id) {
		this.criteria_id = criteria_id;
	}
	public String getTotal_year_of_exp() {
		return total_year_of_exp;
	}
	public void setTotal_year_of_exp(String total_year_of_exp) {
		this.total_year_of_exp = total_year_of_exp;
	}
	public String getTechnology() {
		return technology;
	}
	public void setTechnology(String technology) {
		this.technology = technology;
	}
	
	


}
