package com.java.bean;

public class HRBean {

	private int hr_id;
	private String hr_fname;
	private String hr_lname;
	private String hr_address;
	private String hr_mobileno;
	private String hr_email;
	private String hr_username;
	private String hr_password;
	public int getHr_id() {
		return hr_id;
	}
	public void setHr_id(int hr_id) {
		this.hr_id = hr_id;
	}
	public String getHr_fname() {
		return hr_fname;
	}
	public void setHr_fname(String hr_fname) {
		this.hr_fname = hr_fname;
	}
	public String getHr_lname() {
		return hr_lname;
	}
	public void setHr_lname(String hr_lname) {
		this.hr_lname = hr_lname;
	}
	public String getHr_address() {
		return hr_address;
	}
	public void setHr_address(String hr_address) {
		this.hr_address = hr_address;
	}
	public String getHr_mobileno() {
		return hr_mobileno;
	}
	public void setHr_mobileno(String hr_mobileno) {
		this.hr_mobileno = hr_mobileno;
	}
	
	public String getHr_email() {
		return hr_email;
	}
	public void setHr_email(String hr_email) {
		this.hr_email = hr_email;
	}
	public String getHr_username() {
		return hr_username;
	}
	public void setHr_username(String hr_username) {
		this.hr_username = hr_username;
	}
	public String getHr_password() {
		return hr_password;
	}
	public void setHr_password(String hr_password) {
		this.hr_password = hr_password;
	}

	
}
